public class Main {
    public static void main(String[] args) {

        //For döngüsü

        for (int i=1;i<=10;i+=2){
            System.out.println(i);

        }
        System.out.println("For döngüsü bitti");

        //while döngüsü
        // i while döngüsünün dışında tanımlanır.
        int i=1;

        while (i<10){
            System.out.println(i);
            i++;

        }
        System.out.println("While döngüsü bitti");
    }
}